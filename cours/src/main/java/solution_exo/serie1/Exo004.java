package solution_exo.serie1;

public class Exo004 {
    public static void main(String args[]){
        //--------------------------------------------------------------------
        // Afficher le résulat de ces opérations sans l'écrire explicitement
        //--------------------------------------------------------------------

        // Additionne 50 et 34
        System.out.println(50 + 34);

        // Soustrait 50 à 34
        System.out.println(50 - 34);

        // Mutiplie 4 par 80
        System.out.println( 4 * 80);

        // Divise 80 par 4
        System.out.println( 80 / 4);

        // 50 modulo 3

        System.out.println( 50 % 3 );

        int i = 9;
        // Ajoute 1 à la variable i
        System.out.println(i = i + 1);


        // Retire 2 à la variable i
        System.out.println( i = i - 2);
    }
}
