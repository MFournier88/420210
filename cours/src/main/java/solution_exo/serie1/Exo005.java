package solution_exo.serie1;

public class Exo005 {
    public static void main(String args[]){
        int demo = 0;
        //--------------------------------------------------------------------
        // Afficher le résulat de ces opérations sans l'écrire explicitement
        //--------------------------------------------------------------------

        // Assigne 8 à demo
        demo = 8;
        System.out.println(demo);


        // Augmente la valeur de  demo de 100
        demo += 100;
        System.out.println(demo);


        // Reduit la valeur de demo de 46
        demo -= 46;
        System.out.println(demo);


        // Multiplie la valeur actuel de demo par 5
        demo *= 5;
        System.out.println(demo);


        // Divise la valeur actuel de demo par 2
        demo /= 2;
        System.out.println(demo);


        // Assigne à demo le modulo 4 de sa valeur actuelle
        demo %= 4;
        System.out.println(demo);
    }
}
