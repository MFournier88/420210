package solution_exo.serie1;
public class Exo046 {

    public static void main(String[] args) {
        // Créer une variable de type double
        double valeurDouble = 9.78;

        // Convertir la valeur en int en utilisant un cast explicite
        int valeurInt = (int) valeurDouble;

        // Afficher les deux valeurs avant et après la conversion
        System.out.println("Valeur initiale (double) : " + valeurDouble);
        System.out.println("Valeur convertie (int) : " + valeurInt);
    }
}

